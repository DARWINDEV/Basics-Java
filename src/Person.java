public class Person {
    String nombre;
    String fechaNacimiento;

    void dormir(){
        System.out.println("Soy " + nombre + " y estoy dumiendo");
    }

    void respirar(){
        System.out.println("Soy " + nombre + " y estoy respirar desde " + fechaNacimiento);
    }

    void comer(){
        System.out.println("Soy " + nombre + " y estoy comiendo");
    }
}
